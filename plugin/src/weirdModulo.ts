/**
 *  Custom modulo designed to making selecting lines easier for humans
 *
 * ╔═══════════╦═════════════╦════════╦══════════════════════════════════════════════════════════════════════════╗
 * ║ selection ║ total lines ║ result ║                                                                          ║
 * ╠═══════════╬═════════════╬════════╬══════════════════════════════════════════════════════════════════════════╣
 * ║ 2         ║ 20          ║ 2      ║ second line                                                              ║
 * ╠═══════════╬═════════════╬════════╬══════════════════════════════════════════════════════════════════════════╣
 * ║ 1         ║ 20          ║ 1      ║ first line                                                               ║
 * ╠═══════════╬═════════════╬════════╬══════════════════════════════════════════════════════════════════════════╣
 * ║ 0         ║ 20          ║ 1      ║ imaginary concept, line 0 doesnt exist in the universe of line selection ║
 * ╠═══════════╬═════════════╬════════╬══════════════════════════════════════════════════════════════════════════╣
 * ║ -1        ║ 20          ║ 20     ║ last line of file                                                        ║
 * ╠═══════════╬═════════════╬════════╬══════════════════════════════════════════════════════════════════════════╣
 * ║ -2        ║ 20          ║ 19     ║ second last line                                                         ║
 * ╚═══════════╩═════════════╩════════╩══════════════════════════════════════════════════════════════════════════╝
 */
export function weirdModulo(value = 0, total = 0): number {
	if (value === total) {
		return total;
	}

	if (value >= 0) {
		return (value + total) % total;
	}

	return ((value + total) % total) + 1;
}
